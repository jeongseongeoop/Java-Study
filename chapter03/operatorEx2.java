// 증감 연산자 ++, --

package chapter03;

public class operatorEx2{
    public static void main(String[] args){
        int gameScore= 150;
        
        int gameScore1 = ++gameScore;
        System.out.println(gameScore1);

        int gameScore2 = --gameScore;
        System.out.println(gameScore2);

        
    }
}