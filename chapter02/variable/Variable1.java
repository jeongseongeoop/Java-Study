package chapter02.variable;

public class Variable1 {
    public static void main(String [] args){
        int level;  // 정수형 변수 level을 선언
        level = 10; // 변수 level에 값 10을 대입
        System.out.println(level); // level값 출력
	
        // 1분 복습문제
        int age;
        age = 20;
        System.out.println(age);
    }
}









